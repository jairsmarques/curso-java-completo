
public class OperadoresBitwise {

	public static void main(String[] args) {
		
		// Operador & -> Operação "E" bit a bit
		// Operador | -> Operação "OU" bit a bit
		// Operador ^ -> Operação "OU - Exclusivo" bit a bit
		
		int n1 = 89;
		int n2 = 60;
		
		System.out.println(n1 & n2);
		System.out.println(n1 | n2);
		System.out.println(n1 ^ n2);
		

	}

}
